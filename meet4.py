# belajar aritmatika dasar
x = 9
y = 4

# contoh penjumlahan
hasil = x + y
print("x + y =", hasil)

# pengurangan
hasil = x - y
print("x - y =", hasil)

# perkalian
hasil = x * y
print("x * y =", hasil)

# pembagian
hasil = x / y
print("x / y =", hasil)

# perpangkatan exponen **
hasil = x ** y
print("x ^ y =", hasil)

# modus %
hasil = y % x
print("x % y =", hasil)

# floor divission (pembulatan kebawah) //
hasil = x // y
print("x // y", hasil)

